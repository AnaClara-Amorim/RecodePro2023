// Estrutura WHILE

//Incremento
let quantidade = 10; 
let contador = 1; 

while(contador <= quantidade){
    console.log(contador++) // Se esquecer do contador, vai entrar em um loop infinito
}

// Decremento
let quantidad = 0; 
let contado = 10; 

while(contado > quantidad){
    console.log(--contado) 
}

// Estrutura FOR

// Incremento
for (let i = 0; i < 10; i++){
    console.log (i)
}

// Decremento
for (let i = 10; i > 0; i--){
    console.log (i)
}

//Incremento de 2 em 2
for (let i = 10; i > 0; i=i-2){
    console.log(i)
}