function soma() {

    let x = Number(prompt("Digite o primeiro valor: "))
    let y = Number(prompt("Digite o segundo valor: "))
    let resultado = x + y
    alert("A Soma dos valores: " + resultado)
} 

function sub() {

    let x = Number(prompt("Digite o primeiro valor: "))
    let y = Number(prompt("Digite o segundo valor: "))
    let resultado = x - y
    alert("A Subtração dos valores: " + resultado)
} 

function mult() {
    let x = Number(prompt("Digite o primeiro valor: "))
    let y = Number(prompt("Digite o segundo valor: "))
    let resultado = x * y
    alert("A Multiplicação dos valores: " + resultado)
} 

